# An if statement, if condition is true: do stuff, else: do some other stuff

#The condition is a boolean, (true or false)

#The syntax is:

# if condition:
#   do_some_code

#Examples of conditions:

# x = 6
# x > 5 -> This will return true

# y = 7
# y != 8 -> This wil return true

# z = 20
# z > 21 -> This will return false

# w = 500
# w < 600 -> This will return true

x = 650 

if x != 650:
    print("This is in the if statement")
    print("x is not equal to 650") 
    print("Hello")
print("This is not in the if statement")

y = 60 

if y > 45:
    print("This is in the if statement")
    print("y is bigger than 45")
print("This is not in the if statement")

# We can also compare two variables to each other

var1 = 50

var2 = 60

if var1 > var2:
    print("This is in the if statement - var1 is smaller than var2")
else: 
    print("I love Clifford")

