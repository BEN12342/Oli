# A bunch of definitions

# < -> Smaller than
# > -> Greater than

# != -> Not equal to
# == -> Is equal to
# = -> Assigning a value

#Example:

Value1 = 56
Value2 = 70

if Value1 == Value1:
    print("Value1 is equal to Value2")
elif Value1 < Value2:
    print("Value1 is smaller than Value2")
else:
    print("Value1 is not equal to Value2")
